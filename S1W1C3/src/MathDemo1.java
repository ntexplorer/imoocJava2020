public class MathDemo1 {
    public static void main(String[] args) {
        int m = 10;
        int n = 5;
        m += 3;
        n += 5;
        int p = (m + n) / 2;
        int q = m * m * n * n;
        System.out.println("p = " + p);
        System.out.println("q = " + q);
    }
}
