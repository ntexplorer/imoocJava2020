public class TypeExchange {
    public static void main(String[] args) {
        int n = 98;
        char ch = (char) n;
        long var = 190000000;
        double d = var;
        System.out.println("ch = " + ch);
        System.out.println("var = " + var);
        System.out.println("d = " + d);
    }
}
