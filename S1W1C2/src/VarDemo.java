public class VarDemo {
    public static void main(String[] args) {
//        定义两个整型变量x，y
        int x = 0, y;
        System.out.println("x = " + x);
        y = 3;
        System.out.println("y = " + y);
        System.out.print("x");
        System.out.print("\n");
        System.out.println("y");
        double d = 1.23E5;
        float f = 1.23e5f;
        double d1 = .3;
        System.out.println("d = " + d);
        System.out.println("f = " + f);
        System.out.println("d1 = " + d1);
    }
}
