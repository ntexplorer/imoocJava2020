public class FloatDemo2 {
    public static void main(String[] args) {
        float f1 = 98.4f;
        float f2 = f1;
        int n = 55;
        double d1 = 555.3;
        d1 = n;
        System.out.println("f1 = " + f1);
        System.out.println("f2 = " + f2);
        System.out.println("n = " + n);
        System.out.println("d1 = " + d1);
    }
}
